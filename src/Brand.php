<?php
    class Brand
    {

        private $name;
        private $id = null;

        function __construct($name, $id)
        {
            $this->name = $name;
            $this->id = $id;
        }

        function setName($new_name)
        {
            $this->name = (string) $new_name;
        }
        function getName()
        {
            return $this->name;
        }
        function getId()
        {
            return $this->id;
        }

    }
 ?>
